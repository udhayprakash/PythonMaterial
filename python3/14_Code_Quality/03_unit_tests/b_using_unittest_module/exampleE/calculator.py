def addition(n1, n2):
    return float(n1) + float(n2)
    # return int(n1) + int(n2)
    # return n1 + n2


def subtraction(n1, n2):
    return float(n1) - float(n2)


def multiplication(n1, n2):
    return float(n1) * float(n2)


def division(n1, n2):
    return float(n1) / float(n2)